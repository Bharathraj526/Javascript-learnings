let numberOne="20";
let numberTwo=2;
console.log(numberOne,typeof(numberOne));//20 string
console.log(numberOne+numberTwo,typeof(numberOne+numberTwo));//202 string(no implicit convertion)
console.log(numberOne/numberTwo, typeof(numberOne/numberTwo));//10 'number' (implicit convertion possible)
console.log(numberOne*numberTwo, typeof(numberOne*numberTwo));//40 'number' (implicit convertion possible)
console.log(numberOne-numberTwo, typeof(numberOne-numberTwo));//18 'number' (implicit convertion possible)